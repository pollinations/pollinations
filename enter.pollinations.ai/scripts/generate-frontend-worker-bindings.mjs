import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = resolve(import.meta.dirname, "..");
const sourcePath = resolve(root, "worker-bindings.d.ts");
const outputPath = resolve(root, "frontend/src/worker-bindings.d.ts");

const mainModulePattern =
    /\n\s+interface GlobalProps \{\n\s+mainModule: typeof import\("\.\/src\/index"\);\n\s+\}/;

let source = readFileSync(sourcePath, "utf8");

if (!mainModulePattern.test(source)) {
    throw new Error(
        "Could not find Cloudflare.GlobalProps.mainModule in worker-bindings.d.ts",
    );
}

source = source.replace(mainModulePattern, "");
source = source.replace(/^\/\* eslint-disable \*\/\n?/, "");

writeFileSync(
    outputPath,
    [
        "/* eslint-disable */",
        "// Generated by scripts/generate-frontend-worker-bindings.mjs from ../../worker-bindings.d.ts.",
        "// This keeps Cloudflare binding types in sync while stripping GlobalProps.mainModule,",
        '// which imports "./src/index" and pulls the full Worker into frontend type-checking.',
        source,
    ].join("\n"),
);
